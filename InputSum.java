import java.util.*;
public class InputSum{
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        int a=sc.nextInt();
        int b=sc.nextInt();
        int sum =a+b;
        System.out.println(sum);
        int c=sc.nextInt();
        int d=sc.nextInt();
        int product =c*d;
        System.out.println(product);
        // Area of circle
        float f=sc.nextFloat();
        float area=3.14f * f * f;
        System.out.println(area);
    }
}